<!-- Запись в файл. Пусть в корне вашего сайта лежит файл test.txt. Запишите в него текст '12345'. -->
<?php
file_put_contents('test.txt', '12345');
?>

<!-- Определение размера файла. Пусть в корне вашего сайта лежит файл test.txt. Узнайте его размер, выведите на экран. -->
<?php
$filename = 'test.txt';

if (file_exists($filename)) {
    $size = filesize($filename);
    echo "Размер файла '$filename': $size байт";
} else {
    echo "Файл '$filename' не найден.";
}
?>


<!-- Перемещение файлов. Пусть в корне вашего сайта лежит файл test.txt. Пусть также в корне вашего сайта лежит папка dir. Переместите файл в эту папку. -->
<?php
rename('test.txt', 'dir/test.txt');
?>


<!-- Проверка существования файла. Проверьте, лежит ли в корне вашего сайта файл test.txt. -->
<?php
if (file_exists('test.txt')) {
    echo "Файл test.txt существует.";
} else {
    echo "Файл test.txt не найден.";
}
?>


<!-- Функция file. Дан файл test.txt. В нем на каждой строке написано какое-то число. С помощью функции file найдите сумму этих чисел и выведете ее на экран. -->
<?php
$lines = file('test.txt');
$sum = 0;

foreach ($lines as $line) {
    $sum += (int)trim($line);
}

echo "Сумма чисел: $sum";
?>

